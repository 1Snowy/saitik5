function b1_on(){
    element_but_r=document.querySelector("#pic1_right")
    element_but_l=document.querySelector("#pic1_left")
    element_but_l.style.display="block"
    element_but_r.style.display="block"
}
function b1_off(){
    element_but_r=document.querySelector("#pic1_right")
    element_but_l=document.querySelector("#pic1_left")
    element_but_l.style.display="none"
    element_but_r.style.display="none"
}